#include <unistd.h>

int				verif_colors(const char *s)
{
	if (s[0] == 'e' && s[1] == 'o' && s[2] == 'c' && s[3] == '}')
		return (1);
	else if (s[0] == 'w' && s[1] == 'h' && s[2] == 'i'
			&& s[3] == 't' && s[4] == 'e' && s[5] == '}')
		return (1);
	else if (s[0] == 'r' && s[1] == 'e' && s[2] == 'd' && s[3] == '}')
		return (1);
	else if (s[0] == 'g' && s[1] == 'r' && s[2] == 'e'
			&& s[3] == 'e' && s[4] == 'n' && s[5] == '}')
		return (1);
	else if (s[0] == 'y' && s[1] == 'e' && s[2] == 'l' && s[3] == 'l'
			&& s[4] == 'o' && s[5] == 'w' && s[6] == '}')
		return (1);
	else if (s[0] == 'b' && s[1] == 'l' && s[2] == 'u'
			&& s[3] == 'e' && s[4] == '}')
		return (1);
	return (0);
}

const char		*colors(const char *s)
{
	int		i;

	i = 0;
	if (s[0] == 'e' && s[1] == 'o' && s[2] == 'c' && s[3] == '}')
		write(1, "\033[0m", 4);
	else if (s[0] == 'w' && s[1] == 'h' && s[2] == 'i'
			&& s[3] == 't' && s[4] == 'e' && s[5] == '}')
		write(1, "\033[0;1;m", 7);
	else if (s[0] == 'r' && s[1] == 'e' && s[2] == 'd' && s[3] == '}')
		write(1, "\033[31;1;m", 8);
	else if (s[0] == 'g' && s[1] == 'r' && s[2] == 'e'
			&& s[3] == 'e' && s[4] == 'n' && s[5] == '}')
		write(1, "\033[32;1;m", 8);
	else if (s[0] == 'y' && s[1] == 'e' && s[2] == 'l' && s[3] == 'l'
			&& s[4] == 'o' && s[5] == 'w' && s[6] == '}')
		write(1, "\033[33;1;m", 8);
	else if (s[0] == 'b' && s[1] == 'l' && s[2] == 'u'
			&& s[3] == 'e' && s[4] == '}')
		write(1, "\033[34;1;m", 8);
	while (s[i] && s[i] != '}')
		i++;
	return (&s[i + 1]);
}
