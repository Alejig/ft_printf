int		isadigit(char c)
{
	return (c >= '0' && c <= '9');
}

int		isaflag(char c)
{
	return (c == '-' || c == '+' || c == '#' || c == ' ' || c == '0');
}

int		isatype(char c)
{
	return (c == 'd' || c == 'i' || c == 'o' || c == 'u' || c == 'x'
			|| c == 'X' || c == 'c' || c == 's' || c == 'p' || c == 'f'
			|| c == '%' || c == 'b' || c == 'U' || c == 'r');
}
